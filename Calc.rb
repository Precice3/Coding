sum = num1 + num2
diff = num1 - num2
prod = num1 * num2
quotient = num1 / num2


puts "The sum of #{num1} and #{num2} is #{sum}"
puts "The difference of #{num1} and #{num2} is #{diff}"
puts "The product of #{num1} and #{num2} is #{prod}"
puts "The quotient of #{num1} and #{num2} is #{quotient}"

